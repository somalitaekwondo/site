				<h3>Somalia gains confidence of WTF</h3>
				
                <p>Somali Taekwondo National Team had a meeting with the president of World Taekwondo Federation, Mr. Chungwon Choue, his assistant Mr. Stephen Donnell and the secretary General Dr. Jean-Marie Ayer. The main issue of the meeting was to recognise the Somali Taekwondo National Team as an Official Taekwondo Federation. After 20 years of civil war, Somalia started to grow through the sport, and Taekwondo has been the main sport after some positive international results.</p>  
