                <p><small>Thursday, April 16, 2015</small></p>

                <p>Faisal Jeylani Aweys has not seen his native Somalia since he fled 14 years ago, but he lives in hope of winning an Olympic medal for his war-torn country.</p>

                <p>And Awey's chosen sport of taekwondo has a way of throwing up medals for countries in trouble such as Afghanistan -- so why not Somalia?</p>
                <p>On his part the 2014 IOCâ€™s trophy winner has thanked Somali Olympic Committee for recognizing his hard work and presenting this outstanding award.</p>
                <p>Aweys grew up in a country at war and his mother died from cancer when he was still a small boy. He left with a sister for Switzerland at the age of 13. "I discovered a life, that of an adolescent, because up to then, my life had only been running away."</p>
                <p>Inspired by his mother who had been a taekwondo athlete, Aweys also took up the martial art.</p>
                <p>"My French teacher knew a bit of my story and told me to 'go for this'. Out of respect for her, I did it. It was a way to get closer to my mother."</p>
                <p>Now he teaches taekwondo in Lausanne and is ranked 60th in the world in the under-58 kilos category.</p>
                <p>In Switzerland, Aweys quickly became a black belt and qualified to become a referee and coach.</p>
                <p>"For a long time, I fought in the name of my club, not Switzerland, as I did not have a passport,." he said.</p>

                <img src="img/photo/somali_exile_fights_for_olympic_dream-2.jpg" alt="" />
                <br/>
                <small>Faisal Jeylani Aweys (R) fights with Andre Mota during a training session on April 15, 2015 in Lausanne (AFP Photo/Fabrice Coffrini)</small>
                <br/>
                <br/>
                <p>Married and with a young child, Aweys decided to halt competition in 2010 because he thought sport would dominate his family life.</p>

                <p>But the secretary general of the Somali Olympic Committee, Duran Farah, went to the Lausanne Open tournament and Aweys' life changed again.</p>

                <p>"He was looking for top level athletes. At first I was not very keen, but I had no real excuse not to fight, so I said yes."</p>
                <strong>- Country needs heroes -</strong>
                <br/>
                <p>Aweys won tournaments in France, Switzerland and Germany. And in 2013 he went to Egypt for his first tournament outside of Europe as a Somali athlete. There were eight in the delegation and two were exiles in Europe.</p>
                <p>Aweys came fourth and realised he could compete at an internaitonal level.</p>
                <p>"The work paid off," Aweys said.</p>
                <p>At the African championships in Tunisia, he beat rivals from Gabon and Mozambique before losing in the semi-final to the Egyptian favourite.</p>
                <p>A bronze medal inspired him and now, with money from the Olympic solidarity programme, he hopes to compete in the 2016 Games in Rio.</p>
                <p>To get an automatic place, Aweys must qualify at the African Games in Brazzaville in September or in a small number of qualifying tournaments after. Or he must get an invitation.</p>
                <p>But Aweys says he is determined "to go to the Games under my own steam".</p>
                <p>Success in Rio would give the long-suffering East African country of 10 million people a first Olympic medal.</p>
                <p>"Anything is possible," he said with a smile.</p>
                <p>Rohullah Nikpa won Afghanistan's first Olympic medals in at the 2008 Bejing Games and 2012 in London. Hadi Saei of Iran won two Olympic golds in the sport. "So why not me ..."</p>
                <p>If that dream came true, Aweys would join Abdi Bile, the 1987 world 1,500 metres champion, and British-Somali 5,000m and 10,000m Olympic champion Mo Farah among a rare number of sporting heroes for a country that badly needs them.</p>

