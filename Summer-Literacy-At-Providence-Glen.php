                    <h4>About ESL Afterschool Communities</h4>
                    <p>ESL Afterschool Communities (ESLAsC) is an acclaimed outreach initiative of Columbus State Community College’s Center for Workforce Development. ESLAsC serves the city's large immigrant and refugee population by providing a safe, caring environment where children develop academic, social, and personal skills that last a lifetime. Providence Glen is one of three ESLAsC sites in Columbus. Each is located within an apartment complex, encouraging a sense of community and eliminating the need for transportation. For more information, visit <a href="http://www.cscc.edu/workforce/eslas" rel="external">http://www.cscc.edu/workforce/eslas</a>.</p>
