    				<h3>THE SOMALI TAEKWONDO NATIONAL TEAM PARTICIPATED AND WON BRANZE MEDAL IN THE AFRICAN TAEKWONDO CHAMPIONSHIPS 2014 IN TUNIS, TUNISIA.</h3>
    				
                    <p>The Somali Taekwondo National athlete – Faisal Aweys 58Kg category representing the country has won Bronze Medal and became the third best in the African Taekwondo Championship 2014 held in Tunis, Tunisia. Faisal was continuously winning points against all of his opponents - countries like South Africa, Libya and Gabon and lost the fourth fight against Egypt, for instance Faisal won over fourteen points in his third fight against Gabon” Said the Somali Taekwondo National Team Head Coach Elton de Souza.</p>  

