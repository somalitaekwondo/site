					<p>"Thanks to their success they will be able to take part at the upcoming Taekwondo World Championships to be held in Mexico 15-21 July 2013 and Somalia is very hopeful of more tangible achievements" the Somali NOC secretary general emphasised.</p>
<p>"The achievement of the first ever Taekwondo medal for Somalia and the wonder that Somali basketball men's national team showed in January's Africa zone 5 basketball tournament were a clear signal for the return of a full Somalia" the country's National Olympic committee secretary General Duran Ahmed Farah indicated.</p>
<p>"We believe that the new development strategy of our NOC in collaboration with its affiliated national federations will bring the nation of Somalia to great joy and satisfaction" Secretary General Duran Ahmed Farah noted in his press statement Monday.</p>
<p>In January, Faisal Adan who is Somali National Basketball player living in the US A broke FIBA record in terms of points scored in international matches during FIBA Africa Zone V competition. He scored 59 points in the semis against Rwanda.</p>
<p>"We count on the collaboration and contribution from the IOC and the Olympic solidarity which helped us promote sport in the war-devastated country" the NOC secretary General said adding he was very hopeful that the world of Taekwondo will give a particular consideration to the reviving taekwondo in Somalia.</p>	
			
