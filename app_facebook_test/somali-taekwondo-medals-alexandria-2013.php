				<h3>Somalia celebrates its first Taekwondo medal.</h3>
				
                    <p>Emerging from decades of bloodshed and terrorism, Somalia is celebrating for its first ever Taekwondo medal gained during the recent 5th Alexandria International Open, an unprecedented victory which Somali NOC termed as a 'symbol' for the recovery of Somalia which is once again back on track.</p>  
		    <p>"Following Somalia NOC's new development strategy with Somali athletes living abroad, we are happy to inform the world of sport that Somalia Taekwondo
Federation won its first medal in the country's history" Somali NOC secretary General Duran Ahmed Farah said in a press statement here in Mogadishu on Monday evening.</p>
<br />
                   
                    <img src="https://fbcdn-sphotos-g-a.akamaihd.net/hphotos-ak-ash3/150410_10151302541484856_1828782372_n.jpg" width="400">
                    <br />
					<p>The statement noted that two Somali athletes living in Switzerland and their Brazilian coach Mr. Elton De Souza accompanied by Somalia NOC Vice President Ibrahim
Shamo took part in 5th Alexandria International Open that took place from the 20-27 February 2013 in Alexandria, Egypt. Mohamed Deq Abdulle Mohamed won bronze medal in his weight while Faisal Jeylani Aweys finished 4th in 63 kgs.</p>
		    <br />
					
                    <img src="https://fbcdn-sphotos-d-a.akamaihd.net/hphotos-ak-frc3/318292_10151302541324856_2097382824_n.jpg" width="400"><br />
